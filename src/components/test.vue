    <h4>二．环保需知</h4>
    <p class="smallTit"><strong>2.1 油烟的危害</strong></p> 
    <p> 1）油烟的产生</p>
    <p class="ind3">食物烹饪、加工过程中挥发的油脂、有机质及其加热分解或裂解的产物，统称为油烟。
    油烟的产生跟食材和烹饪方式 有着密切的关系：</p>
    <p class="ind4">（1）高油烟食材：使用含有动、植物油脂的食物，使用动物、植物、人造停用油；</p>
    <p class="ind4">（2）高油烟烹饪方式：炒、煎、烤、炸等。</p>
    <p> 2）油烟的成份</p>
    <p class="ind3">厨房油烟是一种成分非常复杂的由气、固、液三相混合而成的气溶胶, 并且会随着食品种类和烹调条件的变化而改变。</p>
    <p class="ind3">厨房油烟的主要化学成分：醛、酮、烃、脂肪酸、醇、芳香族化合物、酮、内酯、杂环化合物等。</p>
    <p> 3）油烟的成份</p>
    <p class="ind3">油烟中包含：气味、PM2.5和挥发性有机物（VOCs）等污染物。（VOCs是灰霾和臭氧的前体物之一）, 并且会随着食品种类和烹调条件的变化而改变。</p>
    <p> 4）油烟的危害</p>
    <p class="ind3">1.吸入烹调油烟气可引起肺部炎症和组织细胞损伤；</p>
    <p class="ind3">2.烹调油烟能影响机体的细胞免疫、巨噬细胞功能、抗肿瘤效应、免疫监视功能，从而使机体的免疫功能下降；</p>
    <p class="ind3">3.烹调油烟中存在着能引起基因突变、DNA损伤、染色体损伤等不同生物学效应的遗传毒性物质。</p>
    
    <p class="smallTit"><strong>2.2 油烟净化处理规定</strong></p> 
    <p> 1) 产生油烟的餐饮企业应按规范安装设置集气罩、排风管道、排风机和油烟净化设备（必须经过环保保护产品认证油烟去除效率≥90%的设备）</p>
    <p> 2) 油烟设备要与排风机联动，先于排风机启动，后于排风机停止。</p>
    <p> 3) 油烟净化设备的运行控制应有专人负责、每日巡查，并做好台账记录，根据油烟净化设备特效/要求定期清洗保养，由具有专业技能的人员或委托第三方负责。</p>
    <p> 4) 安装和使用油烟在线监控设备，并保持正常运行。</p>

