    <p class="smallTit"><strong>2.4 油烟净化设施</strong></p> 
    <p> 1）常见油烟净化技术</p>
    <p class="ind3">净化设备是油烟污染现有的改善方法，国内生产的油烟净化设备主要有四类：</p>
    <p class="ind3">（1）机械式油烟净化设备</p>
    <p class="ind4">工作原理：采用机械过滤、碰撞、吸附或其他机械分离。</p>
    <p class="ind4">特点：设备简单阻力小，净化效果底，一般只有 20%。</p>
    <p class="ind3">（2）静电式油烟净化设备</p>
    <p class="ind4">工作原理：机内设置高频高压电场，使油烟带上电荷，利用正极板的吸附作用去除油烟。</p>
    <p class="ind4">特点：净化效率高，可达 85%以上，阻力小。目前厂家生产的静电式最多，但安全性（易着火）受到质疑。</p>
    <p class="ind4">目前，餐饮业安装使用静电式净化设备主要有2类：</p>
    <p class="ind5">①高效低空油烟净化器</p>
    <p class="ind6">此类油烟净化器采用超高压电源，电压 1.1kv-1.6kv ，利用无外电场荷电粉平流吸附技术，对油烟分子净化效率达到 85%以上。使用过程中每月定期清洗一次，可自行清洗。</p>
    <p class="ind6">据功率大小，处理风量在 4000-50000m3/h ，适合蒸、煮及产生油烟量小的餐饮店使用。</p>
    <p class="ind5">②低空排放式油烟净化器</p>
    <p class="ind6">此类油烟净化器功率大，对油烟分子净化效率达到 95%以上。使用中每月定期清洗一次，可自行清洗。根据功率大小，处理风量在 6000-50000m3/h ，适合煎、炒、炸、蒸、煮类食品制作餐饮店使用</p>
    <p class="ind3">（3）湿式油烟净化设备</p>
    <p class="ind4">工作原理：采用物理与化学相结合的方法，通过在循环水中适量加入改善油烟雾的疏水性能和增加油水混合的乳化性的油污净化药剂，使用水喷淋对油烟进行洗涤、乳化、碰撞、溶解、冷凝转相。</p>
    <p class="ind4">特点：净化效率较高，一般可达 70%-80%，可同时除去异味，但设备的制作工艺、操作条件要求较严格，设备易腐蚀.</p>
    <p class="ind3">（4）复合式油烟净化设备</p>
    <p class="ind4">工作原理：为保障油烟去除达到认定标准的规定，采取以上三种不同处理方法中的两种方式结合制成一体。</p>
    <p class="ind4">特点：净化效率高，高效的分离油烟，达到最大效率的油烟净化效果。</p>
    <p class="ind4">目前，广泛使用的类型为复合式油烟净化一体机。</p>
    <p class="ind4">此类油烟净化器采用机械与静电一体式设计，净化效果可达 95%以上。安装部分组件后具有防火、去味等功能，清洗时需要专业人员操作，防火网每周定期（可自行）清洗一次，净化器及活性炭棉每 6 个月定期清洗、更换一次。根据功率、大小，处理风量在500-9800m3/h ，适合各类食品制作的大、中、小型餐饮店使用。</p>
    
    <p> 2）油烟净化设备的选择</p>
    <p class="ind3">当前餐饮单位普遍存在油烟排放不达标的问题，只有在许可及油烟污染治理中选择合适、达标的油烟净化设备，才是治理油烟污染行之有效的办法。</p>
    <p class="ind3">选择油烟净化设备时，除根据灶眼，选购风量为灶眼数*2000 m3/h 净化设备外设备外，还应当充分考虑餐饮单位规模大小、与居民楼距离、楼层高度以及经营者经济承受能力等因素。</p>
    <p class="ind3">（1）牛肉面、饺子、早餐、熟食加工等油烟产生量少，选址为平房或四层以下建筑物楼下的餐饮店，可以选择安装一体机（图 3），也可以在室内或楼顶安装高效低空油烟净化器，但在二层以上的为保证油烟顺利抽出，应在房顶安装风柜并配套隔音措施，在安装过程中应当在烟罩与油烟净化设备连接管道的合理位置设置专用的积油盒，并确保管道具有良好的密封性。（超出四层管道及净化设备安装费用超过一体机）。</p>
    <p class="ind3">（2）以炒菜为主的小型排挡、茶府及油炸食品制作等产生油烟相对较多，选址为平房或四层以下建筑物楼下的餐饮店，可以选择安装一体机，也可以选择在室内或楼顶安装低空排放式油烟净化器，需配套安装的设施设备如上。</p>
    <p class="ind3">（3）大中型酒店、茶府及在高层商用楼下的小型餐饮单位，应选择安装复合式油烟净化一体机，未设置专用烟道的应当加装去味装置。</p>
    
