    <p class="ind5">污水、废气处理设施的运行维护管理是指：运维人员制定设备清洗计划，对设施的日常检查、设施处理工艺的调整、设备定期检查维护、部件的维修更换、定期清洗油烟净化设备及清理废油废渣、建立日常检查维护记录制度等。</p>
    <p class="ind4">运维要求</p>
    <p class="ind5">（1）废气治理设施</p>
    <p class="ind5">运维者应依照以上清洗标准对油烟净化装置整体进行清洗。装置使用五年以上的，有投诉的，餐饮企业可委托第三方进行监测，监测结果超标的应当改造或者更换。</p>
    <p class="ind5">清洗工作建议委托第三方专业机构维护。</p>
    <p class="ind5">（2）废水治理设施</p>
    <p class="ind5">废水处理装置保持正常使用，并经过预处理后直接排入市政污水处理系统。城市污水管网纳管范围外的，应当达到排放标准后方可排放。</p>
